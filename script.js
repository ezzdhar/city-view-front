function initSwiper(lang) {
    if (window.workSlider) {
        window.workSlider.destroy(true, true);
    }
    const sliderContainer = document.querySelector('.work-slider');
    if (sliderContainer) {
        sliderContainer.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
        window.workSlider = new Swiper('.work-slider', {
            slidesPerView: 1.1,
            centeredSlides: true,
            spaceBetween: 10,
            loop: true,
            speed: 1200,
            autoplay: {
                delay: 3500,
                disableOnInteraction: false,
            },
            allowTouchMove: true,
            navigation: {
                nextEl: '.swiper-button-next',
                prevEl: '.swiper-button-prev',
            },
            breakpoints: {
                768: { slidesPerView: 1.5, spaceBetween: 30 },
                1024: { slidesPerView: 1.7, spaceBetween: 50 },
            },
        });
    }
}

function setLanguage(lang, isInit = false) {
    localStorage.setItem('lang', lang);
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

    const langText = document.getElementById('lang-text');
    if (langText) {
        langText.innerText = lang === 'ar' ? 'EN' : 'AR';
    }

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    if (!isInit) {
        initSwiper(lang);
    }
}

